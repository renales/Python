# Asignacion a r del entero 5
r = 5
print(r)

# Asignar Asignacion a r de 5 unidades mas 5+5
r += 5
print(r)

# Asignar Resta a r de 2 unidades mas 10-2
r -= 2
print(r)

# Asignar Multiplicar r por 2  10*2
r *= 2
print(r)

# Asignar Dividir r por 3  10/3
r /= 3
print(r)

# Asignar Elevar a r al exponente 3  r**3
r = 2
r **= 3
print(r)

# Asignar Resto de la division de r por 3  r%3
r = 2
r %= 3
print(r)